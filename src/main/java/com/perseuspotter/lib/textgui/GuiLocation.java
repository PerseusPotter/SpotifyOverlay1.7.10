package com.perseuspotter.lib.textgui;

public class GuiLocation {
    public double x;
    public double y;
    public double s;
    public int a;
    public boolean b;
    public int c;

    public GuiLocation(double x, double y, double s, int a, boolean b, int c) {
        this.x = x;
        this.y = y;
        this.s = s;
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
